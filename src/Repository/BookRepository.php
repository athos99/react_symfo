namespace App\Repository;

use App\Entity\Book;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\Persistence\ManagerRegistry;
use Doctrine\ORM\Tools\Pagination\Paginator;


/**
 * @extends ServiceEntityRepository<Book>
 *
 * @method Book|null find($id, $lockMode = null, $lockVersion = null)
 * @method Book|null findOneBy(array $criteria, array $orderBy = null)
 * @method Book[]    findAll()
 * @method Book[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class BookRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Book::class);
    }

    public function add(Book $entity, bool $flush = false): void
    {
        $this->getEntityManager()->persist($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }

    public function remove(Book $entity, bool $flush = false): void
    {
        $this->getEntityManager()->remove($entity);

        if ($flush) {
            $this->getEntityManager()->flush();
        }
    }


    /**
     * @param $options
     * @return Paginator
     */
    public function books($options = [])
    {
        $qb = $this->createQueryBuilder('b');
        if ($options['size'] ?? false) {
            $qb->setMaxResults($options['size']);
        }
        if ($options['page'] ?? false) {
            $qb->setFirstResult(($options['size'] ?? 0) * ($options['page'] - 1));
        }
        if ($options['order'] ?? false) {
            foreach ($options['order'] as $key => $order) {
                $qb->addOrderBy($key, $order);
            }
        }
        $query = $qb->getQuery();
//        $sql = $query->getSQL();

        return new Paginator($query, true);
    }


